# #{} permet de créer une indentation entre des chiffres ou des objets.
puts "On va compter le nombre d'heures de travail à THP" #comme tout les puts "": elle affiche un texte.
puts "Travail : #{10 * 5 * 11}" #une indentation multipliant les 3 nombres d'où le 550 affiché.
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #Pareille une indentation multipliée par 60 pour passer en minute.

puts "Et en secondes ?" #voir ligne 1

puts 10 * 5 * 11 * 60 * 60 #multiplication des nombres pour obtenir les secondes

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #voir ligne 1

puts 3 + 2 < 5 - 7 #je pense qu'il s'agit des conditions. Ce à quoi le programme répond "false".

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #indentation
puts "Ça fait combien 5 - 7 ? #{5 - 7}" #indentation

puts "Ok, c'est faux alors !"

puts "C'est drôle ça, faisons-en plus :"

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #indentation pour vérifer les conditions.
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #indentation pour vérifer les conditions.
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" #indentation pour vérifer les conditions.

#le #{} permet/est une indentation.
